  NOMBRE VARCHAR(10) NOT NULL,
  APELLIDO VARCHAR(30) NOT NULL,
  ESTADO VARCHAR(8) NOT NULL CHECK (ESTADO IN ('activo', 'inactivo')),
  EDAD INTEGER NOT NULL
